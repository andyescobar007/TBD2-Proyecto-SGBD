    private void cargarInfoTable(String sql,JTable table){
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{"Propiedades","Valores"});
        try {
            ResultSet rs=database.getConsulta(sql);
            ResultSetMetaData metaData=rs.getMetaData();
            int cantColumnas=metaData.getColumnCount();
            Object row[]=new Object[cantColumnas];
            int con=0;
             while(rs.next()){
                 for (int i = 0; i < cantColumnas; i++) {
                     row[i]=rs.getString(i+1);
                 }
                 
             }
            
            while(con<cantColumnas){
                modelo.addRow(new Object[]{
                    metaData.getColumnName(con+1),row[con]
                });
                con++;
          }
       
            table.setModel(modelo);
            rs.close();         
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
   
    
