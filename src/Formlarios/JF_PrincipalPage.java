    private String eliminarPalabras(String[] palabras, String sentencia) {
        for(String p:palabras){
            if(sentencia.contains(p)){
                sentencia=sentencia.replace(p,"");
            }
        }
        return sentencia;
    }
    private String aplicarSaltosLinea(String[] palabras, String sentencia) {
        for(String p:palabras){
            if(sentencia.contains(p)){
                if(p.equalsIgnoreCase("FROM")){
                    sentencia=sentencia.replace(p,"\n"+p+"\n");
                }else {
                    sentencia=sentencia.replace(p,"\n"+p+"\n");
                }
            }
        }
        return sentencia;
    }
    
