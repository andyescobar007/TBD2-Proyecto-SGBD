    private void btnChangeNameTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeNameTableActionPerformed
        if(txtNewNameTable.getText().length()<2){
            JOptionPane.showMessageDialog(null, "El nombre no valido");
        }else if(txtNameTable.getText().equalsIgnoreCase(txtNewNameTable.getText())){
            JOptionPane.showMessageDialog(null, "Ya existe un tabla con ese nombre");
        }else{
            String sqltemp="RENAME TABLE \""+SCHEMA+"\"."+txtNameTable.getText()+" TO "+txtNewNameTable.getText();
            try {
                System.out.println(sqltemp);
                database.ejecutarOperacion(sqltemp);
                 txtNameTable.setText(txtNewNameTable.getText());
                JOptionPane.showMessageDialog(null, "La tabla ha sido alterada exitosamente");
                txtNewNameTable.setText("");
                cargarTablas();
                cambiarPanelTable(pnlTableMain);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Error al intentar cambiar nombre a la tabla");
                Logger.getLogger(JF_PrincipalPage.class.getName()).log(Level.SEVERE, null, ex);
            }
           
        }
    }//GEN-LAST:event_btnChangeNameTableActionPerformed

